package com.springmvc;

public class AssigntaskModel {

	private int task_id;
	private String task_name;
	private String task_assignedto;
	private String task_description;
	private String task_assigndate;
	private String task_duedate;
	private String task_currentstatus;
	public int getTask_id() {
		return task_id;
	}
	public void setTask_id(int task_id) {
		this.task_id = task_id;
	}
	public String getTask_name() {
		return task_name;
	}
	public void setTask_name(String task_name) {
		this.task_name = task_name;
	}
	public String getTask_assignedto() {
		return task_assignedto;
	}
	public void setTask_assignedto(String task_assignedto) {
		this.task_assignedto = task_assignedto;
	}
	public String getTask_description() {
		return task_description;
	}
	public void setTask_description(String task_description) {
		this.task_description = task_description;
	}
	public String getTask_assigndate() {
		return task_assigndate;
	}
	public void setTask_assigndate(String task_assigndate) {
		this.task_assigndate = task_assigndate;
	}
	public String getTask_duedate() {
		return task_duedate;
	}
	public void setTask_duedate(String task_duedate) {
		this.task_duedate = task_duedate;
	}
	public String getTask_currentstatus() {
		return task_currentstatus;
	}
	public void setTask_currentstatus(String task_currentstatus) {
		this.task_currentstatus = task_currentstatus;
	}
	
	
}
